#include <iostream>
using namespace std;
/* repetição i++ repete o numero
++i que vai pro numero seguinte */

int main(void){
	int i;
	
	i=5;
	cout<<i<<endl;
	cout<<i++<<endl;
	cout<<i<<endl;
	
	cout<<"-----------"<<endl;
	
	i=5;
	cout<<i<<endl;
	cout<<++i<<endl;
	cout<<i<<endl;
	
	cout<<"------------"<<endl;
	
	i=5;
	cout<<i<<endl;
	cout<<i--<<endl;
	cout<<i<<endl;
	cout<<i<<endl;
	
	cout<<"----------------"<<endl;
	
	i=5;
	cout<<i<<endl;
	cout<<--i<<endl;
	cout<<i<<endl;
	cout<<i<<endl;
	cout<<i<<endl;
	cout<<--i<<endl;
	cout<<i<<endl;
	
	return 0;
}
